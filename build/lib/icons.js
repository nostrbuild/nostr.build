export function getIcon(name, classNames = '', stroke = 1.5) {
  let icon = `<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="${stroke}" stroke-linecap="round" stroke-linejoin="round" class="${classNames}">`
  icon += file[name]
  icon += '</svg>'
  return icon
}

export function getIconByMime(mime = 'application/octet-stream', className = '', stroke = 1.5, alt = false) {
  let icon = 'undefined'
  // Generic Image
  if (mime.startsWith('image/')) icon = 'image'
  // Generic Audio
  if (mime.startsWith('audio/')) icon = alt ? 'audioAlt' : 'audio'
  // Generic Video
  if (mime.startsWith('video/')) icon = alt ? 'videoAlt' : 'video'
  // SVG
  if (mime.startsWith('image/') && mime.includes('svg')) icon = 'svg'
  // GIF
  if (mime.startsWith('image/') && mime.includes('gif')) icon = 'gif'
  // PDF
  if (mime.startsWith('application/pdf')) icon = 'text'
  // ZIP, TAR
  if (mime.startsWith('application/zip')) icon = 'archive'
  if (mime.includes('tar')) icon = 'archive'
  // Spreadsheet
  if (mime.includes('spreadsheet')) icon = 'spreadsheet'
  // Text
  if (mime.startsWith('text/')) icon = 'text'
  const svg = getIcon(icon, className, stroke)
  return svg ?? ''
}